// Regenerates src/lib/createdToken.ts from the solc outputs in
// build/createdtoken/CreatedToken.{abi,bin}. Run after recompiling the contract:
//   node scripts/gen-created-token.mjs
// Hardcodes the ABI (JSON text) and bytecode (0x + bin) verbatim so the deploy
// payload is always byte-exact (an odd-length hex string would be rejected by
// the wallet as "hex string must have an even length").
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");
const abiPath = join(root, "build/createdtoken/CreatedToken.abi");
const binPath = join(root, "build/createdtoken/CreatedToken.bin");
const outPath = join(root, "src/lib/createdToken.ts");

const abi = readFileSync(abiPath, "utf8").trim();
const bin = readFileSync(binPath, "utf8").trim();

if (!/^0x[0-9a-fA-F]*$/.test("0x" + bin)) {
  throw new Error("CreatedToken.bin is not clean hex");
}
if (bin.length % 2 !== 0) {
  throw new Error(`CreatedToken.bin has odd length (${bin.length}); recompile`);
}

const ts = `/**
 * Hardcoded ABI + bytecode for the user-deployable CreatedToken contract
 * (contracts/CreatedToken.sol), compiled with the QuantumCoin solc 0.7.6
 * toolchain and bound with abigen. The "Create token" flow deploys this
 * contract via the extension; its constructor mints the full supply to the
 * creator (msg.sender). Standard ERC20 + Ownable (transferOwnership), with
 * SafeMath for all arithmetic.
 *
 * GENERATED by scripts/gen-created-token.mjs from build/createdtoken/CreatedToken.{abi,bin}.
 * Do not edit by hand - re-run the script after recompiling.
 */

import qc from "quantumcoin";

const CREATED_TOKEN_ABI_JSON = ${JSON.stringify(abi)};

export const CREATED_TOKEN_ABI: readonly unknown[] = JSON.parse(CREATED_TOKEN_ABI_JSON);

export const CREATED_TOKEN_BYTECODE = "0x" + ${JSON.stringify(bin)};

let iface: InstanceType<typeof qc.Interface> | null = null;

function createdIface(): InstanceType<typeof qc.Interface> {
  if (!iface) iface = new qc.Interface(CREATED_TOKEN_ABI as unknown[]);
  return iface;
}

/** ABI-encode the CreatedToken constructor args (returns "0x<args>"). */
export function encodeCreatedTokenDeploy(args: [string, string, number, bigint]): string {
  return createdIface().encodeDeploy(args);
}

/** Full deployment calldata: bytecode + ABI-encoded constructor args. */
export function createdTokenDeployData(name: string, symbol: string, decimals: number, totalSupply: bigint): string {
  return CREATED_TOKEN_BYTECODE + encodeCreatedTokenDeploy([name, symbol, decimals, totalSupply]).slice(2);
}
`;

writeFileSync(outPath, ts, "utf8");
console.log(`Wrote ${outPath}`);
console.log(`  abi chars: ${abi.length}`);
console.log(`  bin chars: ${bin.length} (even: ${bin.length % 2 === 0})`);
